#---------------------------------------------------------------OPERADORES LÓGICOS---------------------------------------------------------------#

# Estos operadores nos permiten hacer "cálculos" con valores booleanos (True o False). También se pueden usar en sentencias if, elif... Funcionan de la misma manera que las puertas lógicas correspondientes.


# AND (&)

True & True # True
True & False # False
False & True # False
False & False # False


# OR (|)

True | True # True
True | False # True
False | True # True
False | False # False


# NOT (not)

not True # False
not False # True