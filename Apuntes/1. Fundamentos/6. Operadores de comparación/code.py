# Nos permiten comparar dos datos y nos devuelven true o false. (Algunos de ellos funcionan con todos los tipos de datos, no solo numeros)


a = 10
b = 5


# Igual:

print(f"Igual: {a == b}") # Con 10 y "10", dice false.

# Diferente:

print(f"Diferente: {a != b}") # Con 10 y "10", dice true.

# Menor que:

print(f"Menor que: {a < b}")

# Menor o igual que:

print(f"Menor o igual que: {a <= b}")

# Mayor que:

print(f"Mayor que: {a > b}")

# Mayor o igual que:

print(f"Mayor o igual que: {a >= b}")